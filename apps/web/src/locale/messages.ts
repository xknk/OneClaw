const TABLE: Record<UiLocale, Record<string, string>> = { zh, en };

export function translate(locale: UiLocale, key: string, vars?: Record<string, string>): string {
    let s = TABLE[locale][key] ?? TABLE.zh[key] ?? key;
    if (vars) {
        for (const [k, v] of Object.entries(vars)) {
            s = s.replaceAll(`{${k}}`, v);
        }
    }
    return s;
}
